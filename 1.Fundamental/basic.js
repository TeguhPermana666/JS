// Variable
/*
Var => not recommended
Let => Fleksibel deklaration
Const => Konstant deklaration
*/

let nama = "Arianto";
console.log(`Nama:${nama}`);

const umur =[]; // cant change the value.
console.log(`Umur:${umur}`);

// Operator.

// Assigment opeartor
const a = 10;
const b = 20;
const c = a+b
console.log(`Nilai C:${c}`);

// Comparassion Opeartator, === dibandingkan sampe tipe datanya
console.log("a lebih besar dari b:",a>b);
console.log("a sama dengan sampe tipe data b:",a===b);

// Logical Operator

// And operator
console.log("a lebih besar dari b:",a>b && a===b);
// Or Operator
console.log("a lebih besar dari b:",a>b || a===b);
// Not operator
console.log("a lebih besar dari b:",!a>b);
